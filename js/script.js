// To open and close side nav 
let openSideNav = () => {
    document.querySelector('#side-links').style.width = '100%'
}

let closeSideNav = () => {
    document.querySelector('#side-links').style.width = '0'
}

// To open and close side search 
let openSideSearch = () => {
    document.querySelector('#side-search').style.width = '100%'
}

let closeSideSearch = () => {
    document.querySelector('#side-search').style.width = '0'
}